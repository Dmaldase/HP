// Подключаем библиотеку для ввода и вывода в консоль
#include <iostream>

// Подключаем библиотеку для генерации случайных чисел
#include <cstdlib>

// Подключаем библиотеку для работы со временем (нужно для srand)
#include <ctime>

// Подключаем библиотеку с полезными алгоритмами, в частности std::swap и std::copy
#include <algorithm>


// ---------------------------
// ПУЗЫРЬКОВАЯ СОРТИРОВКА
// ---------------------------

// Функция принимает:
// arr — указатель на первый элемент массива
// n   — количество элементов в массиве
void bubbleSort(int* arr, size_t n) {

    // Внешний цикл — количество проходов по массиву
    // После каждого прохода самый большой элемент "всплывает" в конец
    for(size_t i = 0; i < n - 1; i++) {

        // Внутренний цикл — сравнивает соседние элементы
        // С каждым проходом диапазон уменьшается,
        // потому что конец массива уже отсортирован
        for(size_t j = 0; j < n - i - 1; j++) {

            // Если текущий элемент больше следующего —
            // меняем их местами
            if(arr[j] > arr[j + 1]) {

                // std::swap меняет значения двух переменных
                std::swap(arr[j], arr[j + 1]);
            }
        }
    }
}


// ---------------------------
// СОРТИРОВКА ВЫБОРОМ
// ---------------------------

void selectionSort(int* arr, size_t n) {

    // Перебираем все позиции массива
    for(size_t i = 0; i < n - 1; i++) {

        // Предполагаем, что текущий элемент — минимальный
        size_t min_index = i;

        // Ищем минимальный элемент в неотсортированной части массива
        for(size_t j = i + 1; j < n; j++) {

            // Если найден элемент меньше текущего минимума —
            // запоминаем его индекс
            if(arr[j] < arr[min_index]) {
                min_index = j;
            }
        }

        // Меняем текущий элемент и найденный минимальный местами
        std::swap(arr[i], arr[min_index]);
    }
}


// ---------------------------
// СОРТИРОВКА ВСТАВКАМИ
// ---------------------------

void insertionSort(int* arr, size_t n) {

    // Начинаем со второго элемента,
    // потому что первый элемент считается уже отсортированным
    for(size_t i = 1; i < n; i++) {

        // Запоминаем текущий элемент
        int key = arr[i];

        // Индекс элемента слева от key
        int j = i - 1;

        // Пока не дошли до начала массива
        // и пока элементы больше key — сдвигаем их вправо
        while(j >= 0 && arr[j] > key) {

            // Сдвиг элемента вправо
            arr[j + 1] = arr[j];

            // Переходим к следующему элементу слева
            j--;
        }

        // Вставляем key на правильную позицию
        arr[j + 1] = key;
    }
}


// ---------------------------
// ФУНКЦИЯ ПЕЧАТИ МАССИВА
// ---------------------------

void printArray(int* arr, size_t n) {

    // Выводим каждый элемент массива
    for(size_t i = 0; i < n; i++) {

        // Печать элемента и пробела
        std::cout << arr[i] << " ";
    }

    // Переход на новую строку
    std::cout << "\n";
}


// ---------------------------
// ТОЧКА ВХОДА В ПРОГРАММУ
// ---------------------------

int main() {

    // Инициализируем генератор случайных чисел
    // текущее время используется как seed
    std::srand(std::time(nullptr));

    // Размер массива
    const size_t N = 20;

    // Объявляем массив из 20 целых чисел
    int arr[N];

    // Заполняем массив случайными числами от 0 до 99
    for(size_t i = 0; i < N; i++) {

        // Генерация случайного числа
        arr[i] = std::rand() % 100;
    }

    // Выводим исходный массив
    std::cout << "Initial array\n";
    printArray(arr, N);


    // ---------- ПУЗЫРЁК ----------

    // Создаём копию массива для пузырьковой сортировки
    int arr_bub[N];

    // Копируем исходный массив в arr_bub
    std::copy(arr, arr + N, arr_bub);

    // Сортируем пузырьком
    bubbleSort(arr_bub, N);

    // Вывод результата
    std::cout << "\nAfter bubbleSort:\n";
    printArray(arr_bub, N);


    // ---------- ВЫБОР ----------

    // Создаём копию массива для сортировки выбором
    int arr_sel[N];

    // Копируем исходный массив
    std::copy(arr, arr + N, arr_sel);

    // Сортируем выбором
    selectionSort(arr_sel, N);

    // Вывод результата
    std::cout << "\nAfter selectionSort:\n";
    printArray(arr_sel, N);


    // ---------- ВСТАВКИ ----------

    // Создаём копию массива для сортировки вставками
    int arr_ins[N];

    // Копируем исходный массив
    std::copy(arr, arr + N, arr_ins);

    // Сортируем вставками
    insertionSort(arr_ins, N);

    // Вывод результата
    std::cout << "\nAfter insertionSort:\n";
    printArray(arr_ins, N);


    // Завершаем программу
    return 0;
}
